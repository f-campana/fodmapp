import { writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

import { semanticSlotMap } from "../src/semantic-slot-map.mjs";

const __filename = fileURLToPath(import.meta.url);
const packageRoot = path.resolve(path.dirname(__filename), "..");
const outputFile = path.join(packageRoot, "shared-styles.css");
const foundationOutputFile = path.join(packageRoot, "foundation.css");

function renderDeclarations(indent) {
  let previousGroup = null;
  const lines = [];

  for (const entry of semanticSlotMap) {
    if (previousGroup !== null && previousGroup !== entry.group) {
      lines.push("");
    }

    lines.push(`${indent}${entry.slot}: var(${entry.token});`);
    previousGroup = entry.group;
  }

  return lines.join("\n");
}

const baseDeclarations = renderDeclarations("    ");
const themeDeclarations = renderDeclarations("  ");

function renderCss(tailwindImport, extraBannerLines = []) {
  return [
    "/* This file is generated by packages/tailwind-config/scripts/generate-shared-styles.mjs. Do not edit directly. */",
    ...extraBannerLines,
    "",
    tailwindImport,
    '@import "@fodmapp/design-tokens/css";',
    "",
    "@custom-variant dark (&:where([data-theme=dark], [data-theme=dark] *));",
    "",
    "@layer base {",
    "  :root,",
    '  [data-theme="light"],',
    '  [data-theme="dark"] {',
    "    /* Runtime aliases for CSS that reads token vars directly (outside Tailwind utilities). */",
    baseDeclarations,
    "  }",
    "}",
    "",
    "@theme inline {",
    themeDeclarations,
    "}",
    "",
  ].join("\n");
}

writeFileSync(outputFile, renderCss('@import "tailwindcss" source(none);'));
writeFileSync(
  foundationOutputFile,
  renderCss('@import "tailwindcss";', [
    "/* App-facing Tailwind foundation only. Consumers using @fodmapp/ui must keep importing the matching UI stylesheet separately. */",
  ]),
);
